package first;

public class Bitwise {

	public static void main(String[] args) {
       int x=5;
       int y=9;
       System.out.println(x&y);  // AND ... if 1 1 then 1
       System.out.println(x|y);  // OR  ... if 0 0 then 0
       System.out.println(x^y);  // XOR ... if both same then 0
       
	}

}
